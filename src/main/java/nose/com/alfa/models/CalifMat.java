package nose.com.alfa.models;

public class CalifMat {
    private String nombre;
    private byte alumno_id;
    private byte califacion;
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public byte getAlumno_id() {
        return alumno_id;
    }
    public void setAlumno_id(byte alumno_id) {
        this.alumno_id = alumno_id;
    }
    public byte getCalifacion() {
        return califacion;
    }
    public void setCalifacion(byte califacion) {
        this.califacion = califacion;
    }
    
}
